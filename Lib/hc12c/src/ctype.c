int isalnum (int ch)
{
  return (((ch >= 'a') && (ch <= 'z')) || ((ch >= 'A') && (ch <= 'Z')) || ((ch >= '0') && (ch <= '9')));
}

int isalpha (int ch)
{
  return (((ch >= 'a') && (ch <= 'z')) || ((ch >= 'A') && (ch <= 'Z')));
}

int iscntrl (int ch)
{
  return ( ((0x0 <= ch) && (ch <= 0x1F)) || (ch == 0x7F));
}

int isdigit(int ch)
{
   return ((ch >= '0') && (ch <= '9'));
}

int isgraph (int ch)
{
  return ((ch > ' ') && (ch < 127));
}

int islower (int ch)
{
  return ((ch >= 'a') && (ch <= 'z'));
}

int isprint (int ch)
{
  return ((ch >= ' ') && (ch < 127));
}

int ispunct (int ch)
{
  return (isgraph (ch) && !isalnum (ch)); 
}

int isspace (int ch)
{
  return (((ch >= 9) && (ch <= 13)) || (ch == 32));
}

int isupper (int ch)
{
  return ((ch >= 'A') && (ch <= 'Z'));
}

int isxdigit (int ch)
{
  return (((ch >= '0') && (ch <= '9')) || ((ch >= 'a') && (ch <= 'f')) || ((ch >= 'A') && (ch <= 'F')));
}

int tolower(int ch)
{
   if ((ch >= 'A') && (ch <= 'Z')) 
   {
      ch = (ch - 'A') + 'a';
   }
   return ch;
}

int toupper(int ch)
{
   if ((ch >= 'a') && (ch <= 'z')) 
   {
      ch = (ch - 'a') + 'A';
   }
   return ch;
}


